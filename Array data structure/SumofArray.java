public class SumofArray {
    public static void main(String[] args) {
        int numbers[] = {1,4,8,4,5};
        int sum = 0;
        for (int i = 0; i <numbers.length; i++) {
            sum = numbers[i]+1;
            // System.out.println(numbers[i]);
            System.out.println(sum);
            
        }
    }
}
